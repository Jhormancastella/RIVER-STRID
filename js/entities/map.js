const CHAPTER_MAPS = {
  1: { 0: MAP_CAP1_F0, 1: MAP_CAP1_F1 },
  2: { 0: MAP_CAP2_F0 },
  3: { 0: MAP_CAP3_F0 }
};

let MAP_DATA = MAP_CAP1_F0;

function loadChapterMap(chapter, floor = 0) {
  MAP_DATA = (CHAPTER_MAPS[chapter] && CHAPTER_MAPS[chapter][floor]) || MAP_CAP1_F0;
  // Actualizar dimensiones dinámicamente según el mapa cargado
  CONFIG.MAP.ROWS = MAP_DATA.length;
  CONFIG.MAP.COLS = MAP_DATA[0].length;
}

function getTile(col, row) {
  if (row < 0 || row >= CONFIG.MAP.ROWS || col < 0 || col >= CONFIG.MAP.COLS) return TileType.WALL;
  return MAP_DATA[row][col];
}

function isWalkable(col, row) {
  const t = getTile(col, row);
  
  // En capítulo 3, las tiles DARK (agua) no son caminables, pero BRIDGE sí
  if (GameState.currentChapter === 3 && t === TileType.DARK) {
    return false;
  }
  
  return t !== TileType.WALL && t !== TileType.UPPER;
}

function getStairAt(x, y, chapter, floor) {
  const stairs = STAIR_POSITIONS[chapter];
  if (!stairs) return null;
  return stairs.find(s => s.x === Math.floor(x) && s.y === Math.floor(y) && s.fromFloor === floor) || null;
}
